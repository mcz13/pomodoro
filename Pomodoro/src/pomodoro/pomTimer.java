    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        closeSound();}
        catch(Exception e){
            
        }
        timer=new Timer(1000,new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
             
                
                 if(sec==0){
                     sec=60;
                     min--;
                 }
                 if(min<0){
                  
                        min=0;sec=0;
                      
                        mod++;
                        
                       
                        if(mod%2==0){
                        playSound();
                        min=25;
                        sec=0;
                        pnum++;
                        pNum.setText(""+pnum);
                         timer.stop();
                         lblMin.setText("25");
                         lblSec.setText("00");
                          jButton1.setEnabled(true);
                         JOptionPane.showMessageDialog(rootPane,"Time Up","Stopped",0);
                         
                        }
                        else{
                            playSoundb();
                            min=5;
                            sec=0;
                        
                        }
                       
                 }
                 else{
                     sec--;
                         if(min<10){
                         lblMin.setText("0"+min);
                         if(sec<10){
                             lblSec.setText("0"+sec);
                         }
                         else{
                           lblSec.setText(""+sec);}
                           }
                     else{  
                     lblMin.setText(""+min);
                     if(sec<10){
                     lblSec.setText("0"+sec);
                     }
                     else
                         lblSec.setText(""+sec);
                         
                      }
                 
                   
                 }
            
            }
        });
        timer.start();
        jButton1.setEnabled(false);
    }//GEN-LAST:event_jButton1ActionPerformed

