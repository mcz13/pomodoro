         public void playSound() {
    try {
        AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(new File("C:/startmusic.wav").getAbsoluteFile());
        clip = AudioSystem.getClip();
        clip.open(audioInputStream);
        clip.start();
    } catch(Exception ex) {
        System.out.println("Error with playing sound.");
        ex.printStackTrace();
    }
    
}
       public void playSoundb() {
    try {
        AudioInputStream audioInputStreamb = AudioSystem.getAudioInputStream(new File("C:/break.wav").getAbsoluteFile());
        clipb = AudioSystem.getClip();
        clipb.open(audioInputStreamb);
        clipb.start();
    } catch(Exception e) {
        System.out.println("Error with playing sound.");
        e.printStackTrace();
    }
    
}
